package com.tutorial;

import javax.swing.text.Style;

public class OperatorAssigment {
    public static void main(String[] args){
        //Operator Assigment

        int a=1;
        a = a + 3;
        System.out.println(a);

        int b=3;
        b+=5;
        System.out.println(b);

        b-=4;
        System.out.println(b);

        b*=3;
        System.out.println(b);

        b/=2;
        System.out.println(b);

        b%=5;
        System.out.println(b);
    }
}
